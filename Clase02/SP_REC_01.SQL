CREATE OR REPLACE PROCEDURE SCOTT.SP_GET_EMP
( P_EMPNO SCOTT.EMP.EMPNO%TYPE )
IS
  REC SCOTT.EMP%ROWTYPE;
BEGIN
  
  SELECT * INTO REC
  FROM EMP
  WHERE EMPNO = P_EMPNO;
  
  DBMS_OUTPUT.PUT_LINE('EMPNO: ' || REC.EMPNO);
  DBMS_OUTPUT.PUT_LINE('ENAME: ' || REC.ENAME);

END;

SELECT * FROM SCOTT.EMP;

CALL SCOTT.SP_GET_EMP( 7369 );



