-- BUCLE FOR
-- =========================

CREATE OR REPLACE PROCEDURE SP_FOR_03
IS
  CONT NUMBER(5) := 500;
BEGIN
  DBMS_OUTPUT.PUT_LINE('CONT 1 = ' || CONT);
  FOR CONT IN 1..10 LOOP
    DBMS_OUTPUT.PUT_LINE('CONT = ' || CONT 
      || '    '  || SP_FOR_03.CONT);
  END LOOP;
  DBMS_OUTPUT.PUT_LINE('CONT 2 = ' || CONT);
END;

SET SERVEROUTPUT ON

CALL SP_FOR_03();

