CREATE OR REPLACE PROCEDURE SCOTT.SP_EXCEP_01
( P_EMPNO NUMBER )
IS
  SALARIO NUMBER;
BEGIN
  SELECT SAL INTO SALARIO
  FROM EMP 
  WHERE EMPNO = P_EMPNO;
  DBMS_OUTPUT.PUT_LINE('SALARIO: ' || SALARIO);
EXCEPTION
  WHEN NO_DATA_FOUND THEN
     DBMS_OUTPUT.PUT_LINE('CODIGO NO EXISTE.');
END;

CALL SCOTT.SP_EXCEP_01( 7369 );