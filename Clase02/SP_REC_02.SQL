CREATE OR REPLACE PROCEDURE SCOTT.SP_GET_EMPS
( P_DEPTNO SCOTT.DEPT.DEPTNO%TYPE )
IS
BEGIN
  
  FOR REC IN (SELECT EMPNO, ENAME, SAL
                        FROM EMP
                        WHERE DEPTNO = P_DEPTNO) LOOP
  
    DBMS_OUTPUT.PUT_LINE(REC.EMPNO || ' - ' || REC.ENAME);
  
  END LOOP;

END;

SELECT * FROM SCOTT.EMP;

CALL SCOTT.SP_GET_EMPS( 10 );



